package mobileworkloads.jlagmarker.masking;

public class Rectangle {
	public final int x0;
	public final int y0;
	public final int width;
	public final int height;
	
	public Rectangle(int x0, int y0, int width, int height) {
		this.x0 = x0;
		this.y0 = y0;
		this.width = width;
		this.height = height;
	}
}
